<h4 class="panel-title">Télécharger notre extension de navigateur</h4>

<p>
L'extension est disponible sur Google Chrome et Opera (stable).<br />
<br />
Si vous êtes sur Opera, vous devez posseder l'extension <a target="_blank" class="link" href="https://addons.opera.com/en/extensions/details/install-chrome-extensions/">Install Chrome Extensions</a> qui permet d'avoir des extensions Chrome sur Opera.
<br />
<br />
Il vous suffit ensuite d'aller sur le <a target="_blank" class="link" href="https://chrome.google.com/webstore/detail/ki-oui/hjnalhfbekamlekjmfkndlcofenojbcg">chrome store</a> pour télécharger l'extension.


</p>